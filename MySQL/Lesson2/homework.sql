DROP TABLE IF EXISTS library.book;
CREATE TABLE library.book(
	id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
	nameOfBook VARCHAR(120) NOT NULL,
    description TEXT,
    dateOfPublication DATE DEFAULT '2018-01-01' NOT NULL,
    category VARCHAR(30) NOT NULL,
    isbn VARCHAR(20) UNIQUE NOT NULL,
    numberOfPages INT NOT NULL,
	authorFirstName VARCHAR(45) NOT NULL,
	authorLastName VARCHAR(45) NOT NULL,
	authorEmail VARCHAR(50) UNIQUE NOT NULL,
	authorAge INT NOT NULL,
	authorDateOfBirthday DATE NOT NULL,
	authorAwards  VARCHAR(120)
);

